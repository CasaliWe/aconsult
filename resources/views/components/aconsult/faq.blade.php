<script>
    document.addEventListener('DOMContentLoaded', () => {
        const faqBtns = document.querySelectorAll('.faq-aconsult-btn');

        faqBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                const item = btn.closest('.faq-aconsult-item');
                const resposta = item.querySelector('.faq-aconsult-resposta');
                const icone = item.querySelector('.faq-aconsult-icone i');
                const aberto = !resposta.classList.contains('hidden');

                /* Fecha todos os outros */
                document.querySelectorAll('.faq-aconsult-item').forEach(outroItem => {
                    if (outroItem !== item) {
                        outroItem.querySelector('.faq-aconsult-resposta').classList.add('hidden');
                        outroItem.querySelector('.faq-aconsult-icone i').classList.remove('rotate-45');
                        outroItem.classList.remove('border-marca/30', 'shadow-lg', 'shadow-marca/5');
                        outroItem.classList.add('border-neutral-100');
                    }
                });

                /* Toggle o clicado */
                if (aberto) {
                    resposta.classList.add('hidden');
                    icone.classList.remove('rotate-45');
                    item.classList.remove('border-marca/30', 'shadow-lg', 'shadow-marca/5');
                    item.classList.add('border-neutral-100');
                } else {
                    resposta.classList.remove('hidden');
                    icone.classList.add('rotate-45');
                    item.classList.add('border-marca/30', 'shadow-lg', 'shadow-marca/5');
                    item.classList.remove('border-neutral-100');
                }
            });
        });
    });
</script>
